#include "atom.h"

//------------------------------------------------------------------------------
Atom::Atom(vec position, vec velocity): position(position), velocity(velocity)
{
}
//------------------------------------------------------------------------------
void Atom::setPosition(const vec3 &r)
{
    position = r;
}
//------------------------------------------------------------------------------
void Atom::setVelocity(const vec3 &v)
{
    velocity = v;
}
//------------------------------------------------------------------------------
vec3 Atom::getPosition()
{
    return position;
}
//------------------------------------------------------------------------------
vec3 Atom::getVelocity()
{
    return velocity;
}
//------------------------------------------------------------------------------
